#!/usr/bin/env node
// Build src/standalone.js from src/index.js — keeps the IIFE drop-in
// version in lockstep with the ESM source. Run via:
//   npm run build:standalone
//
// Why a build step: src/standalone.js used to be hand-maintained as
// a near-mirror of src/index.js. Drift was already happening (e.g.
// the `destroyed` flag existed in index.js but not standalone.js
// for a release window). Single source of truth + a 30-line
// transform is cheaper than the audit cost of asymmetric fixes.
//
// What the transform does:
//   1. Strips `export ` keywords from `export function` / `export const`.
//   2. Wraps the whole module in an IIFE with 'use strict'.
//   3. Appends an auto-bootstrap that calls bootstrapOwnifyChat on
//      DOMContentLoaded (or immediately if the DOM is already parsed).
//
// Constraint: src/index.js MUST be self-contained (no imports beyond
// browser globals). If you ever need a third-party dep, it has to
// land at IIFE level too — pick a different distribution shape.

import { readFileSync, writeFileSync } from 'node:fs';

const SRC = new URL('../src/index.js', import.meta.url);
const DST = new URL('../src/standalone.js', import.meta.url);

const src = readFileSync(SRC, 'utf8');

// Sanity guard: imports break the IIFE wrap. If you see this, the
// transform needs a real bundler instead of regex.
if (/^\s*import\s/m.test(src)) {
  console.error('build-standalone: src/index.js has top-level imports — IIFE transform cannot proceed');
  process.exit(1);
}

// Strip `export ` from exported declarations. Top-level `export *`
// or `export { ... }` blocks are not produced by index.js by
// convention; assert here to fail fast if that changes.
if (/^\s*export\s*\{/m.test(src)) {
  console.error('build-standalone: src/index.js uses an `export { ... }` block — transform expects only `export function/const/let/class`');
  process.exit(1);
}
const stripped = src.replace(/^export\s+/gm, '');

const out = `// AUTO-GENERATED from src/index.js by scripts/build-standalone.js
// DO NOT EDIT THIS FILE DIRECTLY. Edit src/index.js and run:
//   npm run build:standalone
//
// Standalone IIFE — what gets served at https://ownify.ai/chat-widget.js
// for zero-config <script> tag use. Auto-bootstraps on DOMContentLoaded.
//
// For npm/build-system use, import { mountOwnifyChat } from
// 'ownify-chat-widget' (ESM, src/index.js).

(function () {
  'use strict';

  // Capture the script tag's CSP nonce at IIFE entry — by the time
  // DOMContentLoaded fires, document.currentScript is null. If the
  // embedding page loaded the script with a nonce (e.g. <script
  // src=".../chat-widget.js" nonce="abc123">), that nonce gets
  // propagated to every widget mount's injected <style> tag without
  // the embedder having to duplicate it on every mount node.
  var __OWNIFY_DEFAULT_STYLE_NONCE = (typeof document !== 'undefined'
    && document.currentScript
    && document.currentScript.nonce) || null;

${stripped}
  // Auto-bootstrap on DOMContentLoaded so a plain <script> tag works
  // without any inline call-out from the embedding page.
  function __ownifyBootstrap() {
    bootstrapOwnifyChat({ styleNonce: __OWNIFY_DEFAULT_STYLE_NONCE });
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', __ownifyBootstrap);
  } else {
    __ownifyBootstrap();
  }
})();
`;

writeFileSync(DST, out);
console.log('built src/standalone.js from src/index.js (' + out.length + ' bytes)');
